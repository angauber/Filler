#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*concat;
	int		i;
	int		j;
	int		len;

	i = 0;
	j = 0;
	if (!s1 | !s2)
		return (NULL);
	len = ft_strlen(s1) + ft_strlen(s2);
	if (!(concat = ft_strnew(len)))
		return (NULL);
	while (s1[i] != '\0')
	{
		concat[i] = s1[i];
		i++;
	}
	while (s2[j] != '\0')
	{
		concat[i] = s2[j];
		i++;
		j++;
	}
	return (concat);
}
